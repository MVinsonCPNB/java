package sessionmgt.httpsessions;

import java.io.Serializable;  // used to save objects to file
import java.math.BigDecimal;  // used for decimal numbers

public class Product implements Serializable {

    private static final long serialVersionUID = -1L;
    private int id;
    private String name;
    private String description;
    private BigDecimal price;

    public Product(int id, String name, String description, BigDecimal price) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

}
